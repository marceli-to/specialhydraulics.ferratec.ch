<h2>Cookie-Definitionen</h2>
<p>Ein Cookie ist eine kleine Datei aus Buchstaben und Zahlen, die wir in Ihrem Browser oder auf der Festplatte Ihres Geräts speichern, also eine Art Computerspeicher.</p>
<p>Erst- und Drittanbieter-Cookies: Ob es sich um ein "Erst-" oder ein "Drittanbieter"-Cookie handelt, hängt von der Domäne ab, die das Cookie platziert.</p>
<ul>
  <li>
    <p>Erstanbieter-Cookies sind Cookies, die von einer Website gesetzt werden, die der Nutzer gerade besucht (z. B. Cookies, die von unserer Website-Domain gesetzt werden).</p>
  </li>
  <li>
    <p>Drittanbieter-Cookies sind Cookies, die von einer anderen Domain als der der vom Nutzer besuchten Website gesetzt werden. Wenn ein Nutzer eine Website besucht und eine andere Einrichtung ein Cookie über diese Website setzt, handelt es sich um ein Drittanbieter-Cookie.</p>
  </li>
</ul>
<p><strong>Dauerhafte Cookies</strong>: Diese Cookies verbleiben auf dem Gerät des Nutzers für den im Cookie angegebenen Zeitraum. Sie werden jedes Mal aktiviert, wenn der Nutzer die Website besucht, die das jeweilige Cookie erstellt hat.</p>
<p><strong>Sitzungscookies</strong>: Diese Cookies ermöglichen es den Betreibern von Websites, die Aktionen eines Nutzers während einer Browsersitzung zu verknüpfen. Eine Browsersitzung beginnt, wenn ein Nutzer das Browserfenster öffnet, und endet, wenn er das Browserfenster schließt. Sitzungscookies werden vorübergehend erstellt. Sobald Sie den Browser schließen, werden alle Sitzungscookies gelöscht.</p>
