<h2>Definizioni dei cookie</h2>
<p>Un cookie è un piccolo file di lettere e numeri che viene memorizzato sul browser o sul disco rigido del dispositivo, come la memoria del computer.</p>
<p>Cookie di prima e terza parte: il fatto che un cookie sia di "prima" o di "terza" parte si riferisce al dominio che lo inserisce.</p>
<ul>
  <li>
    <p>I cookie di prima parte sono quelli impostati da un sito web visitato dall'utente in quel momento (ad esempio, i cookie inseriti dal dominio del nostro sito web).</p>
  </li>
  <li>
    <p>I cookie di terze parti sono quelli impostati da un dominio diverso da quello del sito web visitato dall'utente. Se un utente visita un sito web e un'altra entità imposta un cookie attraverso quel sito web, si tratta di un cookie di terze parti.</p>
  </li>
</ul>
<p><strong>Cookie persistenti:</strong> questi cookie rimangono sul dispositivo dell'utente per il periodo di tempo specificato nel cookie stesso. Vengono attivati ogni volta che l'utente visita il sito web che ha creato quel particolare cookie.</p>
<p><strong>Cookie di sessione:</strong> questi cookie consentono agli operatori del sito web di collegare le azioni di un utente durante una sessione del browser. Una sessione del browser inizia quando l'utente apre la finestra del browser e termina quando la chiude. I cookie di sessione vengono creati temporaneamente. Una volta chiuso il browser, tutti i cookie di sessione vengono eliminati.</p>
