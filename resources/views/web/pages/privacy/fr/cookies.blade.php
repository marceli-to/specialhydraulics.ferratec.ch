<h2>Définitions des cookies</h2>
<p>Un cookie est un petit fichier de lettres et de chiffres que nous stockons dans votre navigateur ou sur le disque dur de votre appareil, comme une mémoire d'ordinateur.</p>
<p>Cookies de première et de tierce partie : le fait qu'un cookie soit de "première" ou de "tierce" partie se réfère au domaine qui place le cookie.</p>
<ul>
  <li>
    <p>Les cookies de première partie sont ceux qui sont placés par un site web visité par l'utilisateur à ce moment-là (par exemple, les cookies placés par le domaine de notre site web).</p>
  </li>
  <li>
    <p>Les cookies de tierce partie sont des cookies placés par un domaine autre que celui du site web visité par l'utilisateur. Si un utilisateur visite un site web et qu'une autre entité place un cookie par l'intermédiaire de ce site web, il s'agit d'un cookie tiers.</p>
  </li>
</ul>
<p><strong>Cookies persistants </strong>: ces cookies restent sur l'appareil de l'utilisateur pendant la période de temps spécifiée dans le cookie. Ils sont activés chaque fois que l'utilisateur visite le site web qui a créé ce cookie.</p>
<p><strong>Cookies de session </strong>: ces cookies permettent aux opérateurs de sites web de relier les actions d'un utilisateur au cours d'une session de navigation. Une session de navigation commence lorsque l'utilisateur ouvre la fenêtre du navigateur et se termine lorsqu'il la ferme. Les cookies de session sont créés temporairement. Lorsque vous fermez le navigateur, tous les cookies de session sont supprimés.</p>
